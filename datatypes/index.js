// Code Snippet: Data Types
// ----- Primitive Data Types -----

let age = 25; // Number (Integer)

let height = 5.9; // Number (Float)

let name = "John"; // String

let isStudent = true; // Boolean

let city; // Undefined

let phoneNumber = null; // Null

let bigNumber = 12345678901234567890n; // BigInt

console.log("Age: ", age, typeof age);
console.log("Height: ", height, typeof height);
console.log("Name: ", name, typeof name);
console.log("isStudent: ", isStudent, typeof isStudent);
console.log("City: ", city, typeof city);
console.log("PhoneNumber: ", phoneNumber, typeof phoneNumber);
console.log("BigNumber: ", bigNumber, typeof bigNumber);


// ----- Non-Primitive Data Types -----

let fruits = ["apple", "banana", "orange"]; // Array

let person = {
  // Object
  name: "John",
  age: 25,
  isStudent: true,
};

console.log("Fruits: ", fruits, typeof fruits);
console.log("Person: ", person, typeof person);
